package main.DataStructures.Arrays;

public class Arrays {
    public static void main(String[] args) {
        int[] intArray = new int[7];
        intArray[0] = 20;
        intArray[1] = 56;
        intArray[2] = 13;
        intArray[3] = 31;
        intArray[4] = 51;
        intArray[5] = 91;
        intArray[6] = 89;

        for (int j : intArray) {
            System.out.println(j);
        }

    }
}
